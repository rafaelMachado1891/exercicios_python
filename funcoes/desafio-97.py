# Crie um programa personalizado em que a linha de formatação acompanhe o comprimento do texto! 
def formatar_linhas_texto(texto):
   var = '-'
   tam = len(texto)
   res = var * tam
   print(res)
   print(texto)
   print(res)
   
   
variavel = str(input('Escreva: '))

formatar_linhas_texto(variavel)


